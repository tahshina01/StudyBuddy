package com.example.frenbot;

public class GroupItem {
    String groupID;
    String groupName;
    String adminID;
    String adminName;

    public GroupItem(String groupID, String groupName, String adminID, String adminName) {
        this.groupID = groupID;
        this.groupName = groupName;
        this.adminID = adminID;
        this.adminName = adminName;
    }
}
